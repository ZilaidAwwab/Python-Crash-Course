# Dictionary
# A dictionary in Python is a collection of key-value pairs. Each key is connected to a value, and you can use a key to access the value associated with that key. A keyâ€™s value can be a number, a string, a list, or even another dictionary.

# Simple dictionary
alien_0 = {"color": "green", "points": 5}


