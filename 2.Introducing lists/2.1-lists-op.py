motorcycles = ['Honda', 'Yamaha', 'Suzuki']

# Adding

# adding (append) 
# 'The append() method makes it easy to build lists dynamically'
motorcycles.append('BMW')
print(motorcycles)


# Inserting

# inserting elements into a list
# 'the insert opens a position in the list and insert the element we passed as a second arg'
motorcycles.insert(2, 'YBR')
print(motorcycles)
